package Calaulator;

public class Calc
{
    public int add(int a, int b)
    {
        return(a+b);
    }
    public int sub(int a, int b)
    {
        return(a-b);
    }
    public int mul(int a, int b)
    {
        return(a*b);
    }
    public double div(double a, double b)
    {
        return(a/b);
    }
}
